public class Combine {

    private static int number = 0;

    private static void combine(int n, int m)

    {
        for (int i = n; i >= m; i--)   // 注意这里的循环范围

        {
            if (m > 1)
                combine(i - 1, m - 1);
            else                     // m == 1, 输出一个组合
            {
                number++;
            }
        }

    }

    public static int getNumber(int m, int n) {
        number = 0;
        combine(n, m);
        return number;
    }
}
